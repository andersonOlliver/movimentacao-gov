package br.com.bb.intranet.supermt.governo.controller.cadastro;

import br.com.bb.intranet.supermt.governo.model.Agencia;
import br.com.bb.intranet.supermt.governo.service.NegocioException;
import br.com.bb.intranet.supermt.governo.service.cadastro.CadastroAgencia;
import java.io.Serializable;
import javax.faces.application.FacesMessage;
import javax.faces.context.FacesContext;
import javax.faces.view.ViewScoped;
import javax.inject.Inject;
import javax.inject.Named;

/**
 *
 * @author Olliver
 */
@Named
@ViewScoped
public class CadastroAgenciaBean implements Serializable {

    private static final long serialVersionUID = 1L;

    @Inject
    private CadastroAgencia cadastro;

    private Agencia agencia;

    public void prepararCadastro() {
        this.agencia = new Agencia();
    }

    public void salvar() {
        FacesContext context = FacesContext.getCurrentInstance();

        try {
            
            this.cadastro.salvar(agencia);

            context.addMessage(null, new FacesMessage("AgÃªncia salva com sucesso!"));
        } catch (NegocioException e) {

            FacesMessage mensagem = new FacesMessage(e.getMessage());
            mensagem.setSeverity(FacesMessage.SEVERITY_ERROR);
            context.addMessage(null, mensagem);
        }
    }

    public Agencia getAgencia() {
        return agencia;
    }

    public void setAgencia(Agencia agencia) {
        this.agencia = agencia;
    }

}
